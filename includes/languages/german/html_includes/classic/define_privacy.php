<p>Easylamps wurde im Jahre 2005 gegründet und gehört bereits zu den europäischen Hauptvertreibern von Projektorlampen. 
</p><br />

<p>Wir arbeiten direkt mit unseren Fachhändlern und Integratoren im audiovisuellen Bereich zusammen, mit dem Ziel, immer eine geeignete Lösung für einen Lampenerstatz zu finden: Original-Ersatzlampen, "Original Inside" Ersatzlampen, kompatible Lampen, kompatible oder Originalersatz-Birnen.
</p><br />

<p>Auf dieser Website finden Sie für jedes Modell eine passende Ersatzmöglichkeit mit entsprechenden Angaben zu Tarifen,  Verfügbarkeit sowie zu Lieferfristen.

</p><br />

<p>Dank unseres bedeutenden Lagerbestandes ist es uns möglich, Lampen in ganz Europa innerhalb von 24 Stunden zu liefern; bei Gro&#946;aufträgen, die eine Nachbestellung unsererseits erfordern, oder bei Bestellungen von Lampen, die nicht im Lager verfügbar sind, können wir aufgrund der  optimalen Struktur und Dynamik unseres Unternehmens eine Lieferfrist von einer Woche einhalten. 
 </p> <br />

<p>Das Unternehmen Easylamps steht mit seinem Service und seiner Erfahrung den Fachhändlern in allen europäischen Ländern zur Verfügung.
</p>
<p>
Es ist uns immer eine Freude, unsere Dienstleistungen  neuen Fachhandelspartnern anzubieten - unabhängig von ihrer geographischen Lage - und bieten, wenn nötig, die entsprechende Transportlösung.  
</p>

