<h2>Umwelt</h2>
Die Projektorlampen beinhalten gefährliche chemische Produkte, wie zum Beispiel Quecksilber. Sie müssen deshalb sehr sorgfältig behandelt werden. Aus diesem Grund folgt Easylamps.eu seit seiner Gründung einer verantwortungsvollen Umweltpolitik und ruft seine Kunden dazu auf, gebrauchte Lampen aufzubewahren. 
<h2>Recycling Ihrer Lampen</h2>
<p>Wir bitten Sie, uns Ihre gebrauchten Lampen zurückzugeben. Easylamps.eu sammelt die gebrauchten Lampen und gibt sie an ein Recycling-Zentrum weiter, welches auf Quecksilberdampflampen spezialisiert ist.  
