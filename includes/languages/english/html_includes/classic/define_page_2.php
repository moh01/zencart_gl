<h2>Environmental Policy</h2>
Projector lamps contain dangerous chemicals such as mercury. They must be treated with great care. For this reason Easylamps.eu has established a policy of inciting its' clients to recoup used lamps. 
<h2>Recycling your lamps</h2>
<p>We invite you to end us your used lamps back. Easylamps.eu takes the used lamps and sends them to a recycling centre specialised in mercury vapour lamps.
