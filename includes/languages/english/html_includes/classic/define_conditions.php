Commande</B></FONT></FONT></FONT></P>
<P STYLE="margin-right: 0.26cm; margin-bottom: 0.26cm"><FONT COLOR="#000000"><FONT FACE="Arial, sans-serif"><FONT SIZE=1 STYLE="font-size: 8pt">2.1
Toute commande passée avec nous constituera une offre
