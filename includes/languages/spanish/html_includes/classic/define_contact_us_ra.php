<h2>Términos y condiciones de devolución</h2>
<p>
Easylamps se reserva todo derecho de ajustar o de exigir gastos suplementarios si la devolución es inválida, o si la lámpara o el empaque han sido deteriorados o dañados. Los productos nuevos deben ser devueltos en su caja original, con su envoltura original, y deben ser enviados <U>dentro de otra caja o paquete</U> para evitar todo daño causado por una etiqueta de expedición.
</p>
<p>
La solicitud de devolución es válida dentro de los 14 días siguientes a la fecha en que fue presentada.
</p>
<h3>Reglamento</h3>
<ul>
<li>El procedimiento de devolución estará completo una vez que un crédito le sea atribuido, no se le enviará un producto nuevo. Si usted necesita una lámpara de reemplazo, le agradeceremos realizar una nueva solicitud.
 </li>
<li>Los gastos de envío por devolución correrán a cargo del cliente. Si Easylamps es responsable por la devolución (lámpara rota durante el envío, o una lámpara errónea enviada), asumiremos el costo en sus totalidad, y entregaremos una compensación, que cubrirá en parte los gastos de envío de la devolución.
 </li>
<li>A excepción de no ser contemplado así en la garantía, todas las lámpara deben ser devueltas en su envoltura de origen, que no deben tener ningún tipo de marca. En caso de que el paquete esté dañado, se podrán cargar gastos de reempaquetamiento.
 </li>
<li>Si la lámpara deja de funcionar dentro del periodo comprendido por la garantía, necesitaremos el número de serie del proyector para poder transferir la queja al fabricante.
 </li>
<li>Si Easylamps no es responsable, el cliente deberá pagar gastos de reempaquetamiento que pueden elevarse entre el 20% y el 40% del costo del producto.
 </li>
<li>Ciertas lámparas no pueden ser devueltas a menos que estén defectuosas. En caso de que usted adquiera alguno de estos productos especiales, será advertido anticipadamente. 
 </li>
<li>Toda lámpara debe ser devuelta dentro de un periodo de 14 días. Una vez transcurrido este periodo, el número RMA (Return Merchandise Authorization) será suprimido y usted deberá solicitar uno nuevo. Base del formulario.
 </li>
</ul>