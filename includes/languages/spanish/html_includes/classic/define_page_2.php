<h2>Política ambiental</h2>
<p>Las lámparas de videoproyector contienen productos químicos peligrosos como el mercurio. Deben ser tratadas con extremo cuidado. Debido a esto, Easylamps.eu tiene, desde su creación, una política de responsabilidad ambiental, invitando a sus clientes a reciclar sus lámparas utilizadas.
<p />
<h2>Reciclaje de sus lámparas</h2>
<p>Invitamos a nuestros clientes a reciclar las lámparas utilizadas. Easylamps.eu recibe las lámparas utilizadas y las transmite a un centro de clasificación y reciclaje especializado en lámparas a vapor de mercurio. </p>
