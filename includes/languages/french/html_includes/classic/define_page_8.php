<h2>DUREE DE VIE : comment allonger la durée de vie de la lampe ? </h2>


<b>Installation des lampes</b>
<br>
Easylamps.eu recommande de ne pas toucher la lampe avec les doigts, et d’utiliser un morceau de tissus pour la manipuler. Des traces de doigts sur la surface de la lampe peuvent provoquer une surchauffe de la lampe à ces endroits, entraînant une panne prématurée. 
<br><br>
<b>Eteindre le vidéo projecteur</b>
<br>
Eteignez votre vidéo projecteur en utilisant la télécommande et non l’interrupteur on/off ni en débranchant le vidéo projecteur. Le vidéo projecteur se met en veille, l’image disparaît et le ventilateur accélère afin de refroidir progressivement la lampe. La température de la lampe en cours de projection est de 1000 degré. Débrancher le projecteur en pleine projection provoque un refroidissement inégal de la surface de la lampe, causant à terme une panne prématurée de celle-ci. 
<br><br>

<b>Maintenance</b>
<br>
Il est recommandé de nettoyer les filtres à air du vidéo projecteur toutes les 100 heures. Si le filtre est très poussiéreux n’hésitez pas à les nettoyer toutes les 50 heures. S’ils sont bloqués par de la poussière, ils freinent la ventilation de la lampe et provoque également une surchauffe de la lampe.
<br><br>
Installer votre vidéo projecteur à une distance minimale de 30cm des murs et plafond afin de permettre à la forte chaleur de se dégager.
<br>
La panne de la lampe peut être liée à un choc mécanique ; le filament chaud de lampe est très fragile, assurez-vous toujours que le projecteur est éteint et la lampe refroidie avant de déplacer le vidéo projecteur. 
<br><br>
Mode « économie » : si votre vidéo projecteur est équipé d’un commutateur « haut/bas », vous pouvez allonger la durée de vie de lampe en le mettant sur la position basse. La luminosité sera un peu moins forte en contre partie.
<br><br>
Mettez régulièrement à jour les derniers firmware du fabricant, ils contribuent favorablement au rallongement de la durée de vie de votre lampe.
